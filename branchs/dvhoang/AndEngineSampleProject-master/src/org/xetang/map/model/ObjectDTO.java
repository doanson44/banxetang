package org.xetang.map.model;

public class ObjectDTO {
	int _id;
	String _name;
	String _strTexture;

	public int getId() {
		return _id;
	}

	public void setId(int id) {
		this._id = id;
	}

	public String getName() {
		return _name;
	}

	public void setName(String name) {
		this._name = name;
	}

	public String getTextures() {
		return _strTexture;
	}

	public void setTextures(String strTexture) {
		this._strTexture = strTexture;
	}
}
